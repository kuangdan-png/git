public class Address {
    private String addId;
    private String addProvinceNo;
    private String addCityNo;
    private String addDistrictNo;
    private String addDetail;

    public String getAddId() {
        return addId;
    }

    public void setAddId(String addId) {
        this.addId = addId;
    }

    public String getAddProvinceNo() {
        return addProvinceNo;
    }

    public void setAddProvinceNo(String addProvinceNo) {
        this.addProvinceNo = addProvinceNo;
    }

    public String getAddCityNo() {
        return addCityNo;
    }

    public void setAddCityNo(String addCityNo) {
        this.addCityNo = addCityNo;
    }

    public String getAddDistrictNo() {
        return addDistrictNo;
    }

    public void setAddDistrictNo(String addDistrictNo) {
        this.addDistrictNo = addDistrictNo;
    }

    public String getAddDetail() {
        return addDetail;
    }

    public void setAddDetail(String addDetail) {
        this.addDetail = addDetail;
    }

    public Address(String addId, String addProvinceNo, String addCityNo, String addDistrictNo, String addDetail) {
        this.addId = addId;
        this.addProvinceNo = addProvinceNo;
        this.addCityNo = addCityNo;
        this.addDistrictNo = addDistrictNo;
        this.addDetail = addDetail;
    }

    public Address() {
    }

    @Override
    public String toString() {
        return "Address{" +
                "addId='" + addId + '\'' +
                ", addProvinceNo='" + addProvinceNo + '\'' +
                ", addCityNo='" + addCityNo + '\'' +
                ", addDistrictNo='" + addDistrictNo + '\'' +
                ", addDetail='" + addDetail + '\'' +
                '}';
    }
}
