package com.medicine.pojo;

import java.util.*;

/**
 * @Auther: 刘涵
 * @Date: 2019-11-25 14:28
 * @Description:会员级别编号
 */
public class MemberGrade {
   /** 会员级别编号
    *             
    * 
    * @pdOid 5044f778-3e7f-48a5-af6c-24c1755bc9d7 */
   private String megNo;
   /** 会员级别名称
    *             
    * 
    * @pdOid 49bf6d19-7583-4c3e-857a-9ab00317bf83 */
   private String megName;
   /** 会员级别排序
    *             
    * 
    * @pdOid 68b7fd9d-df1b-4732-b145-bc59c317b813 */
   private int megSort;
   /** 会员级别描述
    *             
    * 
    * @pdOid f65534b8-9d14-431c-b25c-bb847c4de10e */
   private String megRemark;

   @Override
   public String toString() {
      return "MemberGrade{" +
              "megNo='" + megNo + '\'' +
              ", megName='" + megName + '\'' +
              ", megSort=" + megSort +
              ", megRemark='" + megRemark + '\'' +
              '}';
   }

   public MemberGrade(String megNo, String megName, int megSort, String megRemark) {
      this.megNo = megNo;
      this.megName = megName;
      this.megSort = megSort;
      this.megRemark = megRemark;
   }

   public MemberGrade() {
   }

   public String getMegNo() {
      return megNo;
   }

   public void setMegNo(String megNo) {
      this.megNo = megNo;
   }

   public String getMegName() {
      return megName;
   }

   public void setMegName(String megName) {
      this.megName = megName;
   }

   public int getMegSort() {
      return megSort;
   }

   public void setMegSort(int megSort) {
      this.megSort = megSort;
   }

   public String getMegRemark() {
      return megRemark;
   }

   public void setMegRemark(String megRemark) {
      this.megRemark = megRemark;
   }
}
