public class Page<T> {
    //当前页
    private int page;
    //页大小
    private int limit;
    // 总条数
    private int totalRecord;
    //总页数
    private int totalPage;
    //传递条件
    private T t;

    public Page() {
    }

    public Page(int page, int limit, int totalRecord, int totalPage, T t) {
        this.page = page;
        this.limit = limit;
        this.totalRecord = totalRecord;
        this.totalPage = totalPage;
        this.t = t;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getlimit() {
        return limit;
    }

    public void setlimit(int limit) {
        this.limit = limit;
    }

    public int getTotalRecord() {
        return totalRecord;
    }

    public void setTotalRecord(int totalRecord) {
        this.totalRecord = totalRecord;
    }

    public int getTotalPage() {
        return totalPage;
    }

    public void setTotalPage(int totalPage) {
        this.totalPage = totalPage;
    }

    public T getT() {
        return t;
    }

    public void setT(T t) {
        this.t = t;
    }
}
