package com.medicine.pojo;

import java.util.*;

/**
 * @Auther: 刘涵
 * @Date: 2019-11-25 14:28
 * @Description:部门信息
 */
public class Department {

   /**
    * 部门编号
    */
   private String depNo;
   /**
    * 部门名称
    */
   private String depName;
   /**
    * 部门排序
    */
   private int depSort;
   /**
    * 部门描述
    */
   private String depDecile;
   /**
    *
    * */
   private String duo1;
   /**
    *
    * */
   private String duo2;

   public Department(String depNo, String depName, int depSort, String depDecile, String duo1, String duo2) {
      this.depNo = depNo;
      this.depName = depName;
      this.depSort = depSort;
      this.depDecile = depDecile;
      this.duo1 = duo1;
      this.duo2 = duo2;
   }

   public Department() {
   }

   public String getDepNo() {
      return depNo;
   }

   public void setDepNo(String depNo) {
      this.depNo = depNo;
   }

   public String getDepName() {
      return depName;
   }

   public void setDepName(String depName) {
      this.depName = depName;
   }

   public int getDepSort() {
      return depSort;
   }

   public void setDepSort(int depSort) {
      this.depSort = depSort;
   }

   public String getDepDecile() {
      return depDecile;
   }

   public void setDepDecile(String depDecile) {
      this.depDecile = depDecile;
   }

   public String getDuo1() {
      return duo1;
   }

   public void setDuo1(String duo1) {
      this.duo1 = duo1;
   }

   public String getDuo2() {
      return duo2;
   }

   public void setDuo2(String duo2) {
      this.duo2 = duo2;
   }

   @Override
   public String toString() {
      return "Department{" +
              "depNo='" + depNo + '\'' +
              ", depName='" + depName + '\'' +
              ", depSort=" + depSort +
              ", depDecile='" + depDecile + '\'' +
              ", duo1='" + duo1 + '\'' +
              ", duo2='" + duo2 + '\'' +
              '}';
   }
}
